#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int lim;

	i = 0;
	if (s1 && s2)
	{
		lim = ft_strlen(s1);
		while (s1[i] && s2[i] && s1[i] == s2[i])
			i++;
		if (i == lim && !s1[i] && !s2[i])
			return (1);
	}
	return (0);
}
